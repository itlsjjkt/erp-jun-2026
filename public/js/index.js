$(document).ready(function() {
    $('.select2').select2({
        placeholder: "Silahkan pilih...",
    });
    $('.datepicker').datepicker({
        todayHighlight: 'TRUE',
        autoclose: true,
    });
    $("body input[type=checkbox].switch,body input[type=radio].switch").iCheck({
        checkboxClass: 'custom-switch',
        radioClass: 'custom-switch',
        inheritClass: true,
    });
    $("body input[type=checkbox].checkbox").iCheck({
        checkboxClass: 'custom-check',
        inheritClass: true,
    });
    $("body input[type=radio].radio").iCheck({
        radioClass: 'custom-radio',
        inheritClass: true,
    });

    $('input').keydown( function(e) {
        var key = e.charCode ? e.charCode : e.keyCode ? e.keyCode : 0;
        if(key == 13) {
            e.preventDefault();
            var inputs = $(this).closest('form').find(':input:visible');
            inputs.eq( inputs.index(this)+ 1 ).focus();
        }
    });

    $('input.next').keydown( function(e) {
        var key = e.charCode ? e.charCode : e.keyCode ? e.keyCode : 0;
        if(key == 13) {
            e.preventDefault();
            var inputs = $(this).closest('form').find(':input.next');
            inputs.eq( inputs.index(this)+ 1 ).focus();
        }
    });

    $.fn.selectRange = function(start, end) {
        if(end === undefined) {
            end = start;
        }
        return this.each(function() {
            if('selectionStart' in this) {
                this.selectionStart = start;
                this.selectionEnd = end;
            } else if(this.setSelectionRange) {
                this.setSelectionRange(start, end);
            } else if(this.createTextRange) {
                var range = this.createTextRange();
                range.collapse(true);
                range.moveEnd('character', end);
                range.moveStart('character', start);
                range.select();
            }
        });
    };

    $('input.next_price').keydown( function(e) {
        var key = e.charCode ? e.charCode : e.keyCode ? e.keyCode : 0;
        if(key == 13) {
            e.preventDefault();
            var inputs = $(this).closest('form').find(':input.next_price');
            var element = inputs.eq( inputs.index(this)+ 1 );
            element.focus();
            element.selectRange(1);

        }
    });

    $(document).on('click', "form.reversal button", function(e) {
        var _this = $(this);
        e.preventDefault();
        Swal.fire({
            title: 'Konfirmasi', // Opération Dangereuse
            text: 'Apakah anda yakin untuk mengembalikan Data?', // Êtes-vous sûr de continuer ?
            type: 'error',
            showCancelButton: true,
            confirmButtonColor: 'null',
            cancelButtonColor: 'null',
            confirmButtonClass: 'btn btn-danger',
            cancelButtonClass: 'btn btn-primary',
            confirmButtonText: 'Ya, hapus!', // Oui, sûr
            cancelButtonText: 'Batal', // Annuler
        }).then(res => {
            if (res.value) {
                _this.closest("form").submit();
            }
        });
    });


});


